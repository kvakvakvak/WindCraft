import random


def bedding_desc_tribe(condition):
    if condition >= 80:
        return "мягкая, упругая подстилка из свежего мха. Приятно пахнет лесом, ни один стебель не примят"
    elif condition >= 41:
        return "мох слегка примялся и местами пожух, но подстилка ещё вполне пригодна для сна"
    else:
        return "плоский слежавшийся мох, почти не дающий мягкости. Пахнет затхлостью"


def bedding_debuff_tribe(condition):
    if condition <= 40:
        return "Дебафф: ломота в суставах, усталость после сна."
    return ""


