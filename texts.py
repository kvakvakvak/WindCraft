def walls_desc(condition):
    if condition >= 60:
        return "Плетеные стены крепкие, ветки плотно подогнаны друг к другу. Лагерь надежно защищен."
    elif condition >= 41:
        variants = [
            "Кое-где ветки разошлись, образовав небольшие щели — пока некритично, но заметно.",
            "В одном месте ветка выбилась из плетения и торчит в сторону.",
            "На одной из сторон стены появилась проплешина — лапой можно просунуть.",
            "Плетение держится, но местами потемнело и стало хрупким.",
            "Несколько веток у основания подгнили, стена чуть просела.",
            "Верхний ряд плетения местами разошелся — ветер задувает внутрь лагеря.",
        ]
        return random.choice(variants)
    else:
        return "В стенах зияют дыры. Лагерь открыт для чужаков и непогоды."


def den_desc(structure, condition):
    if structure == "shadow_warriors":
        if condition >= 60:
            return ("плетение из веток покрывает стены норы ровно и плотно, скрывая холодный камень. Внутри тепло и сухо")
        elif condition >= 41:
            return ("кое-где в плетении появились проплешины, из-за которых проглядывает голый камень норы. Сквозняков нет, но стало заметно холоднее")
        else:
            return ("большая часть веток осыпалась или сгнила, каменные стены норы открыты почти полностью. Сыро, холодно, тянет промозглым духом. Риск простуды и белого кашля")
    elif structure == "branches":
        if condition >= 60:
            return "плетение стен и потолка равномерное и плотное, внутри сухо и тепло"
        elif condition >= 41:
            return "кое-где в плетении появились проплешины, ветер иногда задувает внутрь, но в целом держится"
        else:
            return "в стенах и потолке заметные дыры, дождь и холод проникают без труда. Риск простуды и белого кашля"
    else:
        if condition >= 60:
            return "стены гладкие, потолок не крошится, внутри тихо и защищенно"
